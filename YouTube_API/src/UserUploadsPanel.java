        for (int i = 0; i < rows.length; i++) {
            rows[i] = userUploadsTable.convertRowIndexToModel(rows[i]);
        }
        Arrays.sort(rows);
        for(int i=rows.length - 1; i >=0; i--){
            System.out.println(userUploadsTable.getValueAt(rows[i], 2));
            YouTubeActions.deleteVideoEntry(uploadedVideos, (String)userUploadsTable.getValueAt(rows[i], 2));
            model.removeRow(rows[i]);            
        }
