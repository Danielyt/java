    public void setUploadedVideos(Object[] obj){
        uploadedVideos = new Object[obj.length];
        for(int i = 0; i < obj.length; i++)
            uploadedVideos[i] = obj[i];
        fillUserUploadsTable();
    }
    
    public void fillUserUploadsTable(){
        DefaultTableModel model = (DefaultTableModel) userUploadsTable.getModel();
        model.setRowCount(0);
        if(uploadedVideos != null) {
            Object[] videoRow;
            for (int i = 0; i < uploadedVideos.length; i++){
                videoRow = YouTubeActions.getVideoEntryInfo(uploadedVideos[i]);
                if (videoRow != null)
                    model.addRow(videoRow);
            }
        }
    }
